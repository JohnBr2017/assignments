var today = "Monday";




switch(today){
    case "Sunday":
        console.log("wake up at 2pm.")
        break;
    case "Monday":
        console.log("wake up at 3am.")
        break;
    case "Tuesday":
        console.log("wake up at 4am.")
        break;
    case "Wednesday":
        console.log("wake up at 5am.")
        break;
    case "Thursday":
        console.log("wake up at 6am.")
        break;
    case "Friday":
        console.log("wake up at 7am.")
        break;
    case "Saturday":
        console.log("wake up at snooze.")
        break;
    default:
        console.log("invalid day")
}